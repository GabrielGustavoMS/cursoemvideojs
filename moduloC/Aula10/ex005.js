function clicar() {
  var area = window.document.getElementById('area')
  area.innerText = 'Clicou'
  area.style.background = 'red'
}

function entrar() {
  var area = window.document.getElementById('area')
  area.innerText = 'Entrou'
  area.style.background = 'rgb(42, 139, 42)'
}

function sair() {
  var area = window.document.getElementById('area')
  area.innerText = 'Saiu'
  area.style.background = 'rgb(42, 139, 42)'
}
